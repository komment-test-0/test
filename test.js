/**
* @description This function takes two arguments `alpha` and `beta`, and returns
* their Cartesian product (`**`) (i.e., the exponential of `beta` raised to the power
* of `alpha`).
* 
* @param { number } alpha - The `alpha` input parameter is a scalar multiplier that
* gets raised to the power of `beta`.
* 
* @param {  } beta - The `beta` input parameter is used as an exponent to which the
* `alpha` value is raised.
* 
* @returns { any } The function takes two parameters `alpha` and `beta`, and returns
* the result of raising `alpha` to the power of `beta`.
* 
* In other words:
* 
* Output = `alpha ** beta`
*/
const fn = (alpha, beta) => { return alpha ** beta };

